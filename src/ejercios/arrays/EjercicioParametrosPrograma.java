package ejercios.arrays;

/*
 * Mostrar todos los parametros o argumentos del programa
 */
public class EjercicioParametrosPrograma {

	public static void main(String[] args) {
		// inicializacion
		// condicion de corte
		// accion al fin del bloque
		for (int i = 0; i < args.length; i++) {
			System.out.println(args[i]);
		}

	}

}
